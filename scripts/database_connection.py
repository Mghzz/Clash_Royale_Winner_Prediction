def write_table(
    engine: Engine,
    df: pd.DataFrame,
    table_name: str,
    if_exists: str = "replace",
    chunksize: int = 2000,
) -> None:
    df.to_sql(
        table_name,
        engine,
        if_exists=if_exists,
        index=False,
        chunksize=chunksize,
    )


def read_table(engine: Engine, table_name: str) -> pd.DataFrame:
    df = pd.read_sql_table(table_name, engine)
    df.columns = df.columns.map(str)
    return df


def build_match_base_query() -> str:
    def side_player_cols(alias: str, prefix: str) -> str:
        parts = [f'{alias}."{c}" AS "{prefix}_{c}"' for c in PER_SIDE_NUMERIC]
        parts.append(f'{alias}."clan_tag" AS "{prefix}_clan_tag"')
        return ",\n        ".join(parts)

    def side_card_cols(calias: str, prefix: str) -> str:
        return ",\n        ".join(
            f'COALESCE({calias}.{c}, 0) AS "{prefix}_{c}"' for c in PER_SIDE_CARD_AGG
        )

    return f"""
    WITH card_agg AS (
        SELECT
            "match_player_id",
            COUNT(*) AS num_cards,
            AVG("card_level") AS avg_card_level,
            STDDEV_POP("card_level") AS std_card_level,
            MIN("card_level") AS min_card_level,
            MAX("card_level") AS max_card_level,
            SUM(CASE WHEN "card_level" >= 10 THEN 1 ELSE 0 END) AS high_level_cards
        FROM "MatchPlayerCard"
        GROUP BY "match_player_id"
    )
    SELECT
        m."id" AS match_id,
        m."battleTime",
        m."arena_id",
        m."gameMode_id",
        m."tournamentTag",
        m."average_startingTrophies",
        {side_player_cols('w', 'w')},
        wcl."badgeId" AS "w_clan_badge_id",
        {side_card_cols('wc', 'w')},
        {side_player_cols('l', 'l')},
        lcl."badgeId" AS "l_clan_badge_id",
        {side_card_cols('lc', 'l')}
    FROM "Match" m
    JOIN "MatchPlayer" w
      ON w."match_id" = m."id" AND w."is_winner" = TRUE
    JOIN "MatchPlayer" l
      ON l."match_id" = m."id" AND l."is_winner" = FALSE
    LEFT JOIN card_agg wc ON wc."match_player_id" = w."id"
    LEFT JOIN card_agg lc ON lc."match_player_id" = l."id"
    LEFT JOIN "Clan" wcl ON w."clan_tag" = wcl."tag"
    LEFT JOIN "Clan" lcl ON l."clan_tag" = lcl."tag"
    ORDER BY m."id"
    """
