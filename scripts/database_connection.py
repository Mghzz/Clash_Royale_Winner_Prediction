def ensure_schema(engine: Engine) -> None:
    ddl = SCHEMA_PATH.read_text(encoding="utf-8") if SCHEMA_PATH.exists() else SCHEMA_SQL
    with engine.begin() as conn:
        for raw_stmt in ddl.split(";"):
            stmt = "\n".join(
                line for line in raw_stmt.splitlines() if not line.strip().startswith("--")
            ).strip()
            if stmt:
                conn.execute(text(stmt))


def reset_tables(engine: Engine) -> None:
    with engine.begin() as conn:
        conn.execute(
            text(
                'TRUNCATE TABLE "MatchPlayerCard", "MatchPlayer", "Match", "Card", "Clan" RESTART IDENTITY CASCADE;'
            )
        )


